					<h2>Printing Press</h2>
					<!-- This <section> contains text about the history of the printing press. -->
					<section class="history">
						<blockquote cite="https://en.wikipedia.org/wiki/Printing_press">
							<p>A <dfn>printing press</dfn> is a device for applying pressure to an inked surface resting upon a print medium (such as paper or cloth), thereby transferring the ink. Typically used for texts, the invention and spread of the printing press was one of the most influential events in the second millennium revolutionizing the way people conceive and describe the world they live in, and ushering in the period of modernity.</p>
						</blockquote>
						<p><small>Source: <cite><a href="https://en.wikipedia.org/wiki/Printing_press" rel="external" title="Printing press - from Wikipedia, the free encyclopedia">Wikipedia</a></cite></small></p>
						<p>The printing press was developed by Johannes Gutenberg around 1440 in Mainz, Germany, and it was very important to the spread and democratization of knowledge. Although there were other presses at that time, Gutenberg's machine through the use of a mould allowed for the rapid production of lead alloy type pieces. This assembly line method of copying books enabled a single printing press to create as many as 3,600 pages per day. By 1500 over 1,000 Gutenberg presses were operating in Europe, and by 1600 they had created over <em>200 million new books</em>.</p>
						<p>The printing press not only made books affordable for the lower classes, but it helped spark the Age of Enlightenment and facilitated the spread of new and often controversial ideas. In 1518 followers of the German monk Martin Luther used the printing press to copy and disseminate his seminal work <cite>The Ninety-Five Theses</cite>, which jumpstarted the Protestant Reformation and spurred conflicts like the Thirty Years’ War (1618-48). The printing press proved so influential in prompting revolutions, religious upheaval and scientific thought that Mark Twain would later write, <q>What the world is today, good and bad, it owes to Gutenberg.</q></p>
						<p><small>Source: <cite><a href="http://www.history.com/news/history-lists/11-innovations-that-changed-history" rel="external" title="The Printing press - History.com">History.com</a></cite></small></p>
